//-
//initialize the font engine with the screen dimensions
void DemoFramework::FontEngine::Create( int screenWidth, int screenHeight ) {
	if( !m_fontID ) {
		//create the font texture
		glPixelStorei( GL_UNPACK_ALIGNMENT, 1 );
		glGenTextures( 1, &m_fontID );
		glBindTexture( GL_TEXTURE_2D, m_fontID );

		glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
		glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);
		glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
		glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
		glTexImage2D( GL_TEXTURE_2D, 0, GL_LUMINANCE_ALPHA, FONT_TEX_W, FONT_TEX_H,
					  0, GL_LUMINANCE_ALPHA, GL_UNSIGNED_BYTE, m_fontData );
	}

	m_screenWidth = screenWidth;
	m_screenHeight= screenHeight;
}

//-
//being font rendering
void DemoFramework::FontEngine::BeginTextMode( void ) {
	//go into 2D orthographic mode
	glMatrixMode( GL_PROJECTION );
	glPushMatrix( );
		glLoadIdentity( );
		glOrtho( 0.0f, m_screenWidth, m_screenHeight, 0.0f, -1.0f, 1.0f );

		glMatrixMode( GL_MODELVIEW );
		glPushMatrix( );
			glLoadIdentity( );

	//push texture and state bits onto the stack (SLOW)
	glPushAttrib( GL_TEXTURE_BIT | GL_ENABLE_BIT );

	//bind and configure the font texture
	glBindTexture( GL_TEXTURE_2D, m_fontID );
	glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT );
	glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT );
	glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST );
	glTexParameteri( GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST );
	glTexEnvf( GL_TEXTURE_ENV, GL_TEXTURE_ENV_MODE, GL_MODULATE ); 
	glEnable( GL_TEXTURE_2D );

	//eliminate depth testing and culling
	glDisable( GL_DEPTH_TEST );
	glDisable( GL_CULL_FACE );

	glPolygonMode( GL_FRONT_AND_BACK, GL_FILL );

	//eliminate the black "background" for the texture
	glEnable( GL_BLEND );
	glBlendFunc( GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA );
}

//-
//end font rendering
void DemoFramework::FontEngine::EndTextMode( void ) {
	//restore the old matrices and state attributes
			glMatrixMode( GL_PROJECTION );
		glPopMatrix( );
		glMatrixMode( GL_MODELVIEW );
	glPopMatrix( );

	glPopAttrib( );
}

//-
//render a single line of formatted text
void DemoFramework::FontEngine::Print( int x, int y, char *text, ... ) {
	va_list va;
	char parsedString[256];
	int cX, cY;
	int index, len;
	int s, t;

	//make sure that the string actually exists
	if( text == NULL )	return;

	//parse the string
	va_start( va, text );
	    vsprintf( parsedString, text, va );
	va_end( va );

	//set the font color and start rendering the characters
	glColor4fv( m_color );
	glBegin( GL_QUADS );
		cX= x;	cY= y;

		//get the length of the text text
		len= ( int )strlen( parsedString );

		//render the text in the text text
		for( int i=0; i < len; i++ ) {
			if ( ' ' <= parsedString[i] && parsedString[i] <= '~' ) {
				index= parsedString[i] - ' ';
				s= ( index%8 )*FONT_W;
				t= ( index/8 )*FONT_H;

				//lower left corner
				glTexCoord2f( ( float )( s )/FONT_TEX_W,
							  ( float )( t )/FONT_TEX_H );
				glVertex3s( cX, cY, 0 );

				//lower right corner
				glTexCoord2f( ( float )( s )/FONT_TEX_W,
							  ( float )( t + FONT_H + 1 )/FONT_TEX_H );
				glVertex3s( cX, cY + FONT_DRAW_H, 0 );

				//upper right corner
				glTexCoord2f( ( float )( s + FONT_W )/FONT_TEX_W,
							  ( float )( t + FONT_H + 1)/FONT_TEX_H );
				glVertex3s( cX + FONT_DRAW_W, cY + FONT_DRAW_H, 0 );

				//upper left corner
				glTexCoord2f( ( float )( s + FONT_W )/FONT_TEX_W,
							  ( float )( t )/FONT_TEX_H );
				glVertex3s( cX + FONT_DRAW_W, cY, 0 );

				cX+= FONT_DRAW_W;
			}
		}
	glEnd( );
}

